#include "comprimento.h"

// Função para converter metros para centímetros
float metro_para_centimetro(float metro) {
    return metro * 100.0;
}

// Função para converter metros para milímetros
float metro_para_milimetro(float metro) {
    return metro * 1000.0;
}

// Função para converter centímetros para metros
float centimetro_para_metro(float centimetro) {
    return centimetro / 100.0;
}

// Função para converter centímetros para milímetros
float centimetro_para_milimetro(float centimetro) {
    return centimetro * 10.0;
}

// Função para converter milímetros para metros
float milimetro_para_metro(float milimetro) {
    return milimetro / 1000.0;
}