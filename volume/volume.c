#include "volume.h"

// Função para converter litro para mililitro
float litro_para_mililitro(float litro) {
    return litro * 1000.0;
}

// Função para converter mililitro para litro
float mililitro_para_litro(float mililitro) {
    return mililitro / 1000.0;
}

// Função para converter litro para metro cúbico
float litro_para_metro_cubico(float litro) {
    return litro / 1000.0;
}

// Função para converter metro cúbico para litro
float metro_cubico_para_litro(float metro_cubico) {
    return metro_cubico * 1000.0;
}

